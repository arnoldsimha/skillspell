import {
  Injectable,
  Inject,
  NotFoundException,
  BadRequestException,
  Logger,
} from '@nestjs/common';
import {
  EVAL_REPOSITORY,
  type IEvalRepository,
  type EvalCase,
} from '@skillspell/shared';
import { SkillsService } from '../skills/skills.service.js';
import type { CreateEvalCaseDto } from './dto/create-eval-case.dto.js';
import type { UpdateEvalCaseDto } from './dto/update-eval-case.dto.js';

/**
 * Handles CRUD operations for eval cases (test cases).
 *
 * Extracted from EvalService to follow single-responsibility.
 */
@Injectable()
export class EvalCaseService {
  private readonly logger = new Logger(EvalCaseService.name);

  /** Maximum number of eval cases allowed per skill. */
  static readonly MAX_EVAL_CASES_PER_SKILL = 50;

  constructor(
    @Inject(EVAL_REPOSITORY)
    private readonly evalRepository: IEvalRepository,
    private readonly skillsService: SkillsService,
  ) {}

  /**
   * Create a new eval case for a skill.
   *
   * Ownership is enforced by SkillOwnerGuard via @CheckOwnership on the route.
   */
  async createEvalCase(
    skillId: string,
    dto: CreateEvalCaseDto,
  ): Promise<EvalCase> {
    this.logger.log(`createEvalCase called for skill ${skillId}`);

    // Enforce per-skill limit
    const existingCases = await this.evalRepository.getEvalCases(skillId);
    if (existingCases.length >= EvalCaseService.MAX_EVAL_CASES_PER_SKILL) {
      throw new BadRequestException(
        `Maximum of ${EvalCaseService.MAX_EVAL_CASES_PER_SKILL} test cases per skill reached. Please delete some existing test cases before adding new ones.`,
      );
    }

    const { version } = await this.skillsService.findById(skillId);
    const currentVersion = version ?? 1;

    const evalCase: EvalCase = {
      id: '', // will be generated by the repository
      skillId,
      name: dto.name,
      prompt: dto.prompt,
      expectedOutput: dto.expectedOutput,
      assertions: dto.assertions ?? [],
      context: dto.context,
      maxOutputTokens: dto.maxOutputTokens,
      createdAtVersion: currentVersion,
      createdAt: '',
      updatedAt: '',
    };

    return this.evalRepository.createEvalCase(evalCase);
  }

  /**
   * List all eval cases for a skill.
   *
   * Ownership is enforced by SkillOwnerGuard via @CheckOwnership on the route.
   */
  async getEvalCases(skillId: string): Promise<EvalCase[]> {
    this.logger.log(`getEvalCases called for skill ${skillId}`);
    return this.evalRepository.getEvalCases(skillId);
  }

  /**
   * Update an existing eval case.
   *
   * Ownership is enforced by SkillOwnerGuard via @CheckOwnership on the route.
   */
  async updateEvalCase(
    skillId: string,
    evalId: string,
    dto: UpdateEvalCaseDto,
  ): Promise<EvalCase> {
    this.logger.log(
      `updateEvalCase called for eval ${evalId} in skill ${skillId}`,
    );

    // Single-item lookup by composite key (efficient — no scan)
    const existing = await this.evalRepository.getEvalCaseBySkillAndId(
      skillId,
      evalId,
    );
    if (!existing) {
      throw new NotFoundException(
        `Eval case ${evalId} not found for skill ${skillId}`,
      );
    }

    const updated: EvalCase = {
      ...existing,
      name: dto.name ?? existing.name,
      prompt: dto.prompt ?? existing.prompt,
      expectedOutput:
        dto.expectedOutput !== undefined
          ? dto.expectedOutput
          : existing.expectedOutput,
      assertions: dto.assertions ?? existing.assertions,
      context: dto.context !== undefined ? dto.context : existing.context,
    };

    const result = await this.evalRepository.updateEvalCase(updated);

    return result;
  }

  /**
   * Delete an eval case.
   *
   * Ownership is enforced by SkillOwnerGuard via @CheckOwnership on the route.
   */
  async deleteEvalCase(skillId: string, evalId: string): Promise<void> {
    this.logger.log(
      `deleteEvalCase called for eval ${evalId} in skill ${skillId}`,
    );

    // Single-item lookup by composite key (efficient — no scan)
    const existing = await this.evalRepository.getEvalCaseBySkillAndId(
      skillId,
      evalId,
    );
    if (!existing) {
      throw new NotFoundException(
        `Eval case ${evalId} not found for skill ${skillId}`,
      );
    }

    // Use the efficient composite-key delete (defined on IEvalRepository)
    await this.evalRepository.deleteEvalCaseBySkillAndId(skillId, evalId);

    // Invalidate cached benchmark snapshots (lazy re-compute on next read)
    try {
      await this.evalRepository.deleteBenchmarkSnapshots(skillId);
    } catch (err) {
      this.logger.warn(
        `Failed to invalidate benchmark cache after deleting eval case ${evalId}: ${err}`,
      );
    }
  }

  /**
   * Bulk-create eval cases for a skill.
   *
   * Creates all provided cases in a single request for better performance.
   * Used after the user reviews AI-generated test cases and clicks "Save All".
   *
   * Ownership is enforced by SkillOwnerGuard via @CheckOwnership on the route.
   *
   * @param skillId - The skill to create eval cases for
   * @param cases - Array of eval case data to create
   * @returns Array of created EvalCase objects
   */
  async bulkCreateEvalCases(
    skillId: string,
    cases: Array<{
      name: string;
      prompt: string;
      expectedOutput?: string;
      assertions: Array<{
        type: string;
        value: string;
        description?: string;
      }>;
      context?: string;
      maxOutputTokens?: number;
    }>,
  ): Promise<EvalCase[]> {
    this.logger.log(
      `bulkCreateEvalCases called for skill ${skillId}, ${cases.length} cases`,
    );

    // Enforce per-skill limit
    const existingCases = await this.evalRepository.getEvalCases(skillId);
    const totalAfter = existingCases.length + cases.length;
    if (totalAfter > EvalCaseService.MAX_EVAL_CASES_PER_SKILL) {
      const remaining = EvalCaseService.MAX_EVAL_CASES_PER_SKILL - existingCases.length;
      throw new BadRequestException(
        `Cannot create ${cases.length} test cases. This skill already has ${existingCases.length} test cases and the maximum is ${EvalCaseService.MAX_EVAL_CASES_PER_SKILL}. You can add up to ${Math.max(0, remaining)} more.`,
      );
    }

    const { version } = await this.skillsService.findById(skillId);
    const currentVersion = version ?? 1;

    // Create all eval cases in parallel for better performance
    const created = await Promise.all(
      cases.map((caseData) => {
        const evalCase: EvalCase = {
          id: '',
          skillId,
          name: caseData.name,
          prompt: caseData.prompt,
          expectedOutput: caseData.expectedOutput,
          assertions: caseData.assertions.map((a) => ({
            type: a.type as EvalCase['assertions'][0]['type'],
            value: a.value,
            description: a.description,
          })),
          context: caseData.context,
          maxOutputTokens: caseData.maxOutputTokens,
          createdAtVersion: currentVersion,
          createdAt: '',
          updatedAt: '',
        };
        return this.evalRepository.createEvalCase(evalCase);
      }),
    );

    this.logger.log(
      `bulkCreateEvalCases complete for skill ${skillId}: ${created.length} cases created`,
    );

    return created;
  }
}
